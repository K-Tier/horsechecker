public class Horse {
    /**
     * 馬名
     */
    private String name;
    
    /**
     * 性別
     * 牡馬，牝馬，騙馬の3種
     */
    private String sex;

    /**
     * 年齢
     */
    private int age;

    /**
     * 馬体重
     * XXX.Xkg
     */
    private double weight;

    /**
     * 騎手
     */
    private String jockey;

    /**
     * 斤量
     * XX.0kg
     */
    private double toCarry;

    /**
     * 馬番
     * 1 <= x <= 18
     */
    private int number;

    /**
     * 馬別Net競馬URL
     */
    private String url;
}
